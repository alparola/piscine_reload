int	ft_strcmp(const char *s1, const char *s2)
{
	char	*r1;
	char	*r2;

	r1 = (char *)s1;
	r2 = (char *)s2;
	while (*r1 && *r2 && *r1 == *r2)
	{
		r1++;
		r2++;
	}
	return (*r1 - *r2);
}
